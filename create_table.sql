-- EQUIPAMENTO
CREATE TYPE TP_EQUIPAMENTO AS OBJECT (
    CODIGO_EQUIPAMENTO NUMBER,
    NOME VARCHAR2(40),
    TIPO VARCHAR2(40),
    FABRICANTE VARCHAR2(40)
);
/
CREATE TABLE EQUIPAMENTO OF TP_EQUIPAMENTO(
    CODIGO_EQUIPAMENTO PRIMARY KEY
);
/
--FORNECEDOR
CREATE OR REPLACE TYPE TP_ENDERECO AS OBJECT (
	rua VARCHAR2(50),
	bairro VARCHAR2(25),
	numero NUMBER,
	cep VARCHAR2(8)
);
/

CREATE TYPE tp_fone AS OBJECT(
	cod_area VARCHAR2(3),
	numero VARCHAR2(9)
);
/

CREATE TYPE varray_fone AS VARRAY (5) OF tp_fone;
/

CREATE TYPE tp_ramo AS OBJECT(
	nome_ramo VARCHAR2(40)
);
/

CREATE TYPE varray_ramo AS VARRAY (5) OF tp_ramo;
/

CREATE TYPE TP_FORNECEDOR AS OBJECT (
    CNPJ_FORNECEDOR NUMBER,
    NOME VARCHAR2(40),
    ENDERECO TP_ENDERECO,
    TELEFONE varray_fone,
    RAMO varray_ramo
);
/

CREATE TABLE TB_FORNECEDOR OF TP_FORNECEDOR(
    CNPJ_FORNECEDOR PRIMARY KEY
);
/
